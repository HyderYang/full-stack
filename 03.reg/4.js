/**
 * 元字符 - []
 *      
 *      1. 任何一个
 *          /a[abc]q/ aaq, abq, acq
 *      2. 范围:
 *          /[a-z]/
 *          /[3-5 9]/
 *      3. 排除
 *          /[^+]/
 * 
 * 转义:
 *      \d [0-9]
 *      \w [a-z0-9_]
 *      \s 空白
 *      
 *      \D [^0-9]
 *      \W [^a-z0-9_]
 *      \S 非空白
 * 
 * 量词:
 *      {n}
 *          /a{6}/
 *          /\d{11}/
 *      {n, m}
 *          /\d{5, 12}/
 *      {n,}
 *          /\w{6,}/
 * 
 * 
 * . 任何字符
 * /. 转义
 * 
 * + = {1,}
 * ? = {0,1}
 *      
 * 
 */